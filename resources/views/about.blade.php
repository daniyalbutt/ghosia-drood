                        <h1 class="text-center">Introduction</h1>
                        <p>Gosha-e-Durood o Salam is a sanctuary of peace and a center of spirituality where the lamps of Durood o Salam (blessings upon the Prophet ﷺ) and remembrance of Allah are continuously lit. A unique honor of this institution is that the land on which it is established is officially registered under the name of Syeda-tul-Kainat, Lady of Paradise (Salamullah Alaiha).</p>
                        <p>In reality, this is also a gift from Syeda Khatoon-e-Khald (Salamullah Alaiha), who chose this blessed piece of land and honored it with the everlasting association of her sacred name. The foundation stone of Gosha-e-Durood o Salam was laid on the day of Syeda-tul-Kainat’s (Salamullah Alaiha) passing on 3rd Ramadan 1429 Hijri (corresponding to September 5, 2008, Friday).</p>
                        <p>The foundation was laid by the Poet of Islam and Ambassador of the Love of Ahlul Bayt, Al-Haj Muhammad Yasin Ajmal Chishti Sahib, along with his dear friend Haji Sheikh Afzal Ahmed Bashir Sahib. The ceremony took place in the presence of numerous Sayyids, esteemed scholars, and devotees. The first brick of this sacred institution was laid with the completion of the recitation of the Holy Quran, and every brick used in its construction was placed after the recitation of Durood Sharif (salutations upon the Prophet ﷺ).</p>
                        <h3>Objectives and Goals of Gosha-e-Durood o Salam:</h3>
                        <p>Upholding the commandments of Allah as ordained.</p>
                        <p>Observing etiquette and reverence in the court of Prophet Muhammad ﷺ.</p>
                        <p>Promoting love and devotion for the Ahlul Bayt (the noble family of the Prophet ﷺ).</p>
                        <p>Spreading respect and love for the esteemed companions (Sahaba) of the Prophet ﷺ.</p>
                        <p>Honoring all sacred associations related to the Prophet ﷺ.</p>
                        <p>Disseminating the teachings of pious saints and righteous scholars.</p>
                        <p>Reviving the spirit of Sufism.</p>
                        <p>Defending the honor of Ahlul Bayt.</p>
                        <p>Encouraging the recitation of Na’ats (poetry in praise of the Prophet ﷺ) and Manqabat (praise of Ahlul Bayt and Sahaba).</p>
                        <p>Serving humanity through charitable and spiritual efforts.</p>
                        <p>Providing spiritual diagnosis and healing (Fi Sabeelillah – free of charge).</p>
                        <p>Note: This institution does not accept Zakat, charity, or donations.</p>
